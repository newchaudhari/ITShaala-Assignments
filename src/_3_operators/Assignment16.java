package _3_operators;

public class Assignment16 {
    public static void main(String[] args) {
        int a=10;
        int b=5;
        System.out.println(!true);
        System.out.println(~a);
        System.out.println();
        System.out.println(a<<b);
        System.out.println(a>>b);
        System.out.println();
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(a^b);
    }
}
