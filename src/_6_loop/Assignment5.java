package _6_loop;

public class Assignment5 {
    //Print multiplication table of 24, 50 and 29 using loop.
    public static void main(String[] args) {
        int num1=24;
        int num2=50;
        int num3=29;
        for (int i=1;i<=10;i++){
            System.out.print(num1*i+" ");
        }
        System.out.println();
        for (int i=1;i<=10;i++){
            System.out.print(num2*i+" ");
        }
        System.out.println();
        for (int i=1;i<=10;i++){
            System.out.print(num3*i+" ");
        }
    }

}
