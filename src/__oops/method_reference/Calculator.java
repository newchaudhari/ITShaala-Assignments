package __oops.method_reference;

public class Calculator {
    static int add(int n1, int n2){
        return n1+n2;
    }
    int subtract(int n1, int n2){
        return n1-n2;
    }
    int multiply(int n1, int n2){
        return n1*n2;
    }
    int divide(int n1, int n2){
        return n1/n2;
    }
    int remainder(int n1, int n2){
        return n1%n2;
    }
}
